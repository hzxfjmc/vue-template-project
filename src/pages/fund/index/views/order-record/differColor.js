export function differColor(type) {
    let color = ''
    switch (type) {
        case 1:
            color = 'blue-style'
            break
        case 2:
            color = 'yellow-style'
            break
        case 3:
            color = 'grey-style'
            break
        case 4:
            color = 'grey-style'
            break
        case 5:
            color = 'green-style'
            break
        default:
            break
    }
    return color
}
